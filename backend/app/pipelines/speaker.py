"""إسناد هوية المتحدث (طبيب/مريض) لكل مقطع تفريغ — استدلال بالمحتوى + سياق المحادثة.

هذا ليس تفريقاً صوتياً (acoustic diarization) بل تصنيف لغوي خفيف يعمل زمن-حقيقي
داخل P1 دون أي اعتماد خارجي: يقرأ قرائن الكلام العربي السريري (بلهجاته، وقد تتخلله
مقاطع إنجليزية) ويجمعها مع نمط تبادل الأدوار في المحادثة كاملة.

المخرج لكل مقطع: ("doctor" | "patient", confidence ∈ [0,1]).
القاعدة: القرائن المعجمية تحسم غالبية المقاطع؛ عند التقارب فقط يرجّح تبادل الأدوار
(الرد يأتي عادةً من الطرف الآخر) — بوزن ضعيف كي لا يقلب مقطعاً واضح النبرة، لأن
كلام الطبيب (فحص ← تقييم ← خطة) يأتي غالباً في أدوار متتالية.

القرائن مكتوبة بصيغة مُطبَّعة (بلا تشكيل، ألف/همزة/تاء مربوطة موحّدة) لتصمد أمام
اختلاف الرسم واللهجات.
"""
from __future__ import annotations

import re

# إزالة التشكيل والتطويل
_DIACRITICS = re.compile(r"[ؗ-ًؚ-ْٰـ]")
# كلمة لاتينية (مصطلح/دواء إنجليزي) — قرينة على كلام الفاحص السريري
_LATIN_WORD = re.compile(r"[a-z]{3,}")
_ARABIC = re.compile(r"[؀-ۿ]")

Speaker = str  # "doctor" | "patient"

# عتبات الترجيح
_TIE = 1.5          # نطاق "التقارب" الذي يُسمح فيه بترجيح تبادل الأدوار
_ALT = 1.0          # قوة نبضة تبادل الأدوار (نحو الطرف الآخر)
_OPENING_BIAS = 0.6  # الدور الأول يميل للمريض (الزيارة تُفتتح غالباً بتحية/شكوى)
_CONF_SPAN = 8.0     # مقام تطبيع الثقة


def _normalize(text: str) -> str:
    t = _DIACRITICS.sub("", text.strip().lower())
    for src, dst in (("أ", "ا"), ("إ", "ا"), ("آ", "ا"), ("ى", "ي"), ("ة", "ه"), ("ؤ", "و"), ("ئ", "ي")):
        t = t.replace(src, dst)
    return t


# قرائن الطبيب (عبارة مُطبَّعة، وزن) — فحص/قياس/تقييم/خطة/متابعة/مخاطبة المريض بالأمر
_DOCTOR_CUES: tuple[tuple[str, float], ...] = (
    ("خليني اقيس", 2.5), ("اقيس لك", 2.5), ("اقيس", 2.0), ("نقيس", 2.0),
    ("افحص", 2.0), ("نفحص", 2.0), ("الفحص", 2.0), ("فحص القلب", 1.5),
    ("القياس", 2.5), ("النبض", 2.0), ("الحراره", 2.0), ("الضغط", 0.5),
    ("سليم", 1.5), ("طبيعي", 1.5), ("طبيعيه", 1.5), ("مرتفع", 1.5), ("منضبط", 1.5),
    ("التقييم", 2.5), ("التشخيص", 2.5), ("مبدئي", 1.0),
    ("نبدا", 2.0), ("مليجرام", 2.5), ("ملجم", 2.5), ("ملغم", 2.5), (" mg", 2.5),
    ("الجرعه", 2.0), ("جرعه", 1.5), ("مره واحده يوميا", 2.0), ("مرتين يوميا", 1.5), ("مره يوميا", 1.5),
    ("نشوفك", 2.5), ("بعد اسبوعين", 1.5), ("بالمواعيد", 1.5), ("الطوارئ", 1.5), ("تراجع", 1.0),
    ("ابيك", 2.0), ("اريدك", 2.0), ("لازم", 0.75),
    ("وضحت للمريض", 3.0), ("وضحت له", 2.0), ("المريض", 2.0),
    ("تقيس", 1.5), ("تسجل القراءات", 1.5), ("القراءات", 1.0),
)

# قرائن المريض — تحية/مخاطبة الطبيب/شكوى بضمير المتكلم/عدم انتظام مُقرّ به/مدة الشكوى
_PATIENT_CUES: tuple[tuple[str, float], ...] = (
    ("السلام عليكم", 2.0), ("يا دكتور", 2.0), ("يادكتور", 2.0), ("دكتور", 1.5),
    ("احس", 2.0), ("اشعر", 2.0), ("اعاني", 2.0), ("عندي", 1.5), ("اشتكي", 2.0),
    ("يوجعني", 2.0), ("وجعني", 1.5), ("تعبان", 1.5), ("دايخ", 1.5), ("صار لي", 1.5),
    ("دوخه", 1.0), ("يزيد", 1.0), ("معه دوخه", 1.5),
    ("ما كنت منتظم", 2.5), ("مو منتظم", 2.5), ("مب منتظم", 2.5), ("ما التزمت", 2.5),
    ("نسيت", 1.5), ("ما اخذت", 1.5),
    ("من خمس ايام", 1.5), ("من كم يوم", 1.5), ("من اسبوع", 1.5), ("من فتره", 1.0),
    ("بصراحه", 0.5), ("والله", 0.5),
)


def _has_latin_clinical(norm: str) -> bool:
    """مصطلح/دواء لاتيني داخل جملة عربية — نادراً ما ينطقه المريض."""
    return bool(_LATIN_WORD.search(norm)) and bool(_ARABIC.search(norm))


def attribute_speaker(text: str, prev_speaker: Speaker | None = None) -> tuple[Speaker, float]:
    """يصنّف مقطعاً واحداً. موجب ⇒ طبيب، سالب ⇒ مريض؛ التبادل يرجّح التقارب فقط."""
    norm = _normalize(text)
    lex = 0.0
    for cue, weight in _DOCTOR_CUES:
        if cue in norm:
            lex += weight
    for cue, weight in _PATIENT_CUES:
        if cue in norm:
            lex -= weight
    if _has_latin_clinical(norm):
        lex += 1.0

    score = lex
    if abs(lex) < _TIE:  # تقارب — استعن بسياق تبادل الأدوار
        if prev_speaker == "patient":
            score += _ALT
        elif prev_speaker == "doctor":
            score -= _ALT
        else:  # أول دور في الزيارة
            score -= _OPENING_BIAS

    speaker: Speaker = "doctor" if score >= 0 else "patient"
    confidence = round(min(0.99, 0.5 + abs(score) / _CONF_SPAN), 2)
    return speaker, confidence


def attribute_segments(segments: list[dict]) -> list[dict]:
    """يُسند المتحدث لقائمة مقاطع (مسار غير متدفق) مع تمرير سياق الدور السابق."""
    prev: Speaker | None = None
    for segment in segments:
        speaker, confidence = attribute_speaker(segment.get("text", ""), prev)
        segment["speaker"] = speaker
        segment["speaker_confidence"] = confidence
        prev = speaker
    return segments


def label_ar(speaker: Speaker) -> str:
    return "الطبيب" if speaker == "doctor" else "المريض"
